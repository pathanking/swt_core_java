package com.ncs.oop.pack1;

public class A {
    private int i = 10;
    public int j = 20;
    int k = 30;
    protected int m = 40;

    public static void main(String[] args) {
        A obj = new A();
        System.out.println(obj.i);
        System.out.println(obj.j);
        System.out.println(obj.k);
        System.out.println(obj.m);
    }
}

